# Tipo de dado Bool (boolean)
# Serve para representar valores lógicos, ou seja, verdadeiro ou falso.
print(True)  # Verdadeiro
print(False)  # Falso

print(10 == 10)  # True, pois 10 é igual a 10
print(10 != 5)  # True, pois 10 é diferente de 5
print(10 > 5)  # True, pois 10 é maior que 5
print(10 < 5)  # False, pois 10 não é menor que 5
print(10 >= 10)  # True, pois 10 é maior ou igual a 10
print(10 <= 5)  # False, pois 10 não é menor ou igual a 5
print(10 == 5)  # False, pois 10 não é igual a 5
print(type(True))  # <class 'bool'>
