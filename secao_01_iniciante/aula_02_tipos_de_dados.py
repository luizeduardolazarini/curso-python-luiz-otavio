# Aula 02 - Tipos de Dados
# sep significa separador, ou seja, o que vai separar os valores que estão sendo impressos. Por padrão, o separador é um espaço em branco.
# end significa final, ou seja, o que vai ser impresso no final da linha. Por padrão, o final é uma quebra de linha.
print(12, 34, sep=' - ', end=' - ') 
print(56, 78, sep=' - ', end=' - ')
print(9, 10, sep=' - ')

# Aspas simples
print('Olá, mundo!')

# Aspas duplas
print("Olá, mundo!")

# r
print(r'Olá, mundo!')

