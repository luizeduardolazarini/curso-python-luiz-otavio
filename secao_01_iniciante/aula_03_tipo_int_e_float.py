# Tipos int e float 
# int: números inteiros, sem parte decimal
# float: números reais, com parte decimal
# 0 tipo int é usado para representar números inteiros, enquanto o tipo float é usado para representar números reais, que podem ter uma parte decimal.
print(10) # int
print(-10) # int
print(0) # int

print(10.5) # float
print(-10.5) # float
print(0.0) # float

# A função type() pode ser usada para verificar o tipo de uma variável ou valor.
print(type(10)) # <class 'int'>
print(type(10.5)) # <class 'float'>
